    private boolean InputValidation(){
        if(txtFirstName.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Missing input First Name", "SAVE ERROR", JOptionPane.WARNING_MESSAGE);
            return false; 
        }else if(txtLastName.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Missing input Last Name", "SAVE ERROR", JOptionPane.WARNING_MESSAGE);
            return false; 
        }else if(txtContact.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Missing input Contact Number", "SAVE ERROR", JOptionPane.WARNING_MESSAGE);
            return false; 
        }else if (txtSalary.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Missing input Salary", "SAVE ERROR", JOptionPane.WARNING_MESSAGE);
            return false;  
        }else{
            return true;
        }
    }
    
    public void LoadDriverID() {
    	
    	try {
			pst = con.prepareStatement("SELECT driverID FROM driver");
			rs = pst.executeQuery();
			comboDriverID.removeAllItems();
			while(rs .next()) {
				comboDriverID.addItem(rs.getString(1));	
			}
					
		} catch (SQLException ex) {
			// TODO Auto-generated catch block
			Logger.getLogger(Driver.class.getName()).log(Level.SEVERE, null, ex);
		}
    }
    
    private void Fetch() {
    	int a;
    	try {
			pst = con.prepareStatement("SELECT * FROM driver");
			rs = pst.executeQuery();
			ResultSetMetaData rss = rs.getMetaData();
			a = rss.getColumnCount();
			
			
			DefaultTableModel df = (DefaultTableModel)tblDriver.getModel();
			df.setRowCount(0);
			while(rs.next()){
				Vector v2 = new Vector();
				for(int b=1; b<=a; b++) {
					v2.add(rs.getString("driverID"));
					v2.add(rs.getString("firstName"));
					v2.add(rs.getString("lastName"));
					v2.add(rs.getString("contactNo"));
					v2.add(rs.getString("salary"));
				}	
				df.addRow(v2);
			}
			
		} catch (SQLException ex) {
			// TODO Auto-generated catch block
			Logger.getLogger(Driver.class.getName()).log(Level.SEVERE, null, ex);
		}	
    }

