    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        
        if(InputValidation()!=false){
    	String firstName = txtFirstName.getText();
    	String lastName = txtLastName.getText();
    	String contact = txtContact.getText();
    	String salary = txtSalary.getText();
    	String driverID = comboDriverID.getSelectedItem().toString();
    	
    	try {
			pst = con.prepareStatement("UPDATE driver SET firstName=?,lastName=?,contactNo=?,salary=? WHERE driverID=?");
			pst.setString(1, firstName);
			pst.setString(2, lastName);
			pst.setString(3, contact);
			pst.setString(4, salary);
			pst.setString(5, driverID);
			
			int k = pst.executeUpdate();
			
			if(k==1) {
				 JOptionPane.showMessageDialog(this, "Data is update successfully.", "SUCCESS", JOptionPane.INFORMATION_MESSAGE);
				 txtFirstName.setText("");
				 txtLastName.setText("");
				 txtContact.setText("");
				 txtSalary.setText("");
				 comboDriverID.requestFocus();
				 Fetch();
				 	LoadDriverID();
			}else {
				JOptionPane.showMessageDialog(this, "Data is not update successfully.", "ERROR", JOptionPane.ERROR_MESSAGE);
			}
			
		} catch (SQLException ex) {
			// TODO Auto-generated catch block
			Logger.getLogger(Driver.class.getName()).log(Level.SEVERE, null, ex);
		}	
     }
    }//GEN-LAST:event_btnEditActionPerformed

