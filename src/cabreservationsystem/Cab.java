    public final void LoadCabID() {
    	
    	try {
			pst = con.prepareStatement("SELECT cabID FROM cab");
			rs = pst.executeQuery();
			comboCabID.removeAllItems();
			while(rs .next()) {
				comboCabID.addItem(rs.getString(1));	
			}
					
		} catch (SQLException ex) {
			// TODO Auto-generated catch block
			Logger.getLogger(Cab.class.getName()).log(Level.SEVERE, null, ex);
		}
    }
    
    private void Fetch() {
    	int a;
    	try {
			pst = con.prepareStatement("SELECT * FROM cab");
			rs = pst.executeQuery();
			ResultSetMetaData rss = rs.getMetaData();
			a = rss.getColumnCount();
			
			
			DefaultTableModel df = (DefaultTableModel)tblCab.getModel();
			df.setRowCount(0);
			while(rs.next()){
				Vector v2 = new Vector();
				for(int b=1; b<=a; b++) {
					v2.add(rs.getString("cabID"));
					v2.add(rs.getString("cabBrand"));
					v2.add(rs.getString("cabModel"));
				}	
				df.addRow(v2);
			}
			
		} catch (SQLException ex) {
			// TODO Auto-generated catch block
			Logger.getLogger(Cab.class.getName()).log(Level.SEVERE, null, ex);
		}	
    }
    
