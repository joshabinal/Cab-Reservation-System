    private void btnEdit1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEdit1ActionPerformed
        // TODO add your handling code here:
        
        if(InputValidation()!=false){
    	String cabBrand = txtBrand.getText();
    	String cabModel = txtModel.getText();
    	String cabID = comboCabID.getSelectedItem().toString();
    	
    	try {
			pst = con.prepareStatement("UPDATE cab SET cabBrand=?,cabModel=? WHERE cabID=?");
			pst.setString(1, cabBrand);
			pst.setString(2, cabModel);
			pst.setString(3, cabID);
			
			int k = pst.executeUpdate();
			
			if(k==1) {
				 JOptionPane.showMessageDialog(this, "Data is update successfully.", "SUCCESS", JOptionPane.INFORMATION_MESSAGE);
				 txtBrand.setText("");
				 txtModel.setText("");
				 comboCabID.requestFocus();
				 Fetch();
				 	LoadCabID();
			}else {
				JOptionPane.showMessageDialog(this, "Data is not update successfully.", "ERROR", JOptionPane.ERROR_MESSAGE);
			}
			
		} catch (SQLException ex) {
			// TODO Auto-generated catch block
			Logger.getLogger(Cab.class.getName()).log(Level.SEVERE, null, ex);
		}	
      }
    }//GEN-LAST:event_btnEdit1ActionPerformed

